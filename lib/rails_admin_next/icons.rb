    # Build an inline `<svg>` markup string for a logical icon name (see GLYPHS),
    # or nil if the name is unknown. A legacy Font Awesome class string is resolved
    # best-effort and warns via RailsAdminNext.deprecator. The single source of
    # truth for icon markup, shared by IconsHelper (views) and field types (lib).
    def self.svg(name, **html_options)
      return if name.nil?

      if fa_class?(name)
        RailsAdminNext.deprecator.warn(
          "RailsAdminNext icons now use logical names; #{name.inspect} is a Font Awesome class string. " \
          "Font Awesome is no longer shipped — pass a logical name (see RailsAdminNext::Icons::GLYPHS)."
        )
        name = from_fa_class(name)
      end

      key = name && canonical(name)
      glyph = key && GLYPHS[key]
      return unless glyph

      classes = ["rails-admin-icon", html_options.delete(:class)].compact.join(" ")
      attributes = {
        "viewBox" => glyph[:view_box],
        "aria-hidden" => "true",
        "focusable" => "false",
        "data-icon" => key.to_s,
        "class" => classes
      }.merge(html_options.transform_keys(&:to_s))
      rendered = attributes.map { |attr, value| %(#{CGI.escapeHTML(attr.to_s)}="#{CGI.escapeHTML(value.to_s)}") }.join(" ")
      %(<svg #{rendered}><path d="#{CGI.escapeHTML(glyph[:path])}"/></svg>)
    end

    # Resolve a logical name (or alias) to its canonical glyph key, or nil.
    def self.canonical(name)
      key = name.to_sym
      return key if GLYPHS.key?(key)

      ALIASES[key]
    end

    # Does this value look like a (legacy) Font Awesome class string?
    def self.fa_class?(value)
      value.is_a?(String) && value.match?(/\bfa[a-z]?\b|\bfa-[a-z]/)
    end

    # Best-effort canonical name for a legacy `'fas fa-…'` string, or nil. Scans
    # every `fa-…` token so sizing modifiers (e.g. `fa-fw`) don't mask the glyph.
    def self.from_fa_class(value)
      value.to_s.scan(/fa-([a-z0-9-]+)/).flatten.lazy.filter_map { |token| LEGACY_FA[token] }.first
    end
  end
